    private void logHoursButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logHoursButtonActionPerformed

        //get id of selected Volunteer
        int row = jTable1.getSelectedRow();
        int id = (int) jTable1.getValueAt(row, 0);

        //set textfield for hours and date picker
        JXDatePicker dp = new JXDatePicker();
        JTextField hours = new JTextField(5);

        //create jpanel for optionpane
        JPanel myPanel = new JPanel();
        //myPanel.add(new JLabel(String.format("How many hours did %s work?", volunteers.get(id).getFirst_name()), null, JLabel.CENTER));
        myPanel.add(new JLabel("Hours worked:"));
        myPanel.add(hours);
        myPanel.add(Box.createHorizontalStrut(15)); // a spacer
        myPanel.add(new JLabel("Date:"));
        myPanel.add(dp);

        //create optionpane
        int result = JOptionPane.showConfirmDialog(this, myPanel,
                String.format("How many hours did %s work?",
                        volunteers.get(id).getFirst_name()),
                JOptionPane.OK_CANCEL_OPTION);

        //handle input
        if (result == JOptionPane.OK_OPTION) {

            int hrs = Integer.parseInt(hours.getText());

            System.out.println("hours worked: " + hrs);

            //format date
            Date d = dp.getDate();
            DateFormat df = new SimpleDateFormat("yyyy-M-dd");
            String date = df.format(d);

            System.out.println("date: " + date);

            //add hours to database
            doUpdate(String.format(
                    "INSERT INTO `hours` ("
                    + "`volunteer_id`,"
                    + "`hours_worked`,"
                    + "`date`) "
                    + "VALUES ("
                    + "%d, "
                    + "%d, "
                    + "'%s');",
                    id, hrs, date));

            loadHoursTable();
            loadTotalHours();
            loadVolunteersTable();

        } else if (result == JOptionPane.CANCEL_OPTION) {
            //do nothing
        }
    }//GEN-LAST:event_logHoursButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed

        //try block is to make sure we dont get an array out of bounds error
        try {
            //get id of selected row
            int row = jTable1.getSelectedRow();
            int id = (int) jTable1.getValueAt(row, 0);

            //create dialog box
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(this,
                    String.format("Are you sure you wish to delete user: %s %s?", volunteers.get(id).getFirst_name(), volunteers.get(id).getLast_name()),
                    "Delete User",
                    dialogButton);

            //if yes is selected delete contact_info, hours, then volunteer
            //then reload tables of course
            if (dialogResult == JOptionPane.YES_OPTION) {
                doUpdate(String.format("DELETE FROM contact_info WHERE id = %d", id));
                doUpdate(String.format("DELETE FROM hours WHERE volunteer_id = %d", id));
                doUpdate(String.format("DELETE FROM volunteers WHERE id = %d", id));
                loadHoursTable();
                loadTotalHours();
                loadVolunteersTable();
                
            } else if (dialogResult == JOptionPane.NO_OPTION) {
                //do nothing
            }
        } catch (ArrayIndexOutOfBoundsException e) {
            System.out.println("No volunteer selected.");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

